print("\nWant to know the result of raising your first number to the power of your second number?")

first_number = int(input("Please enter your first number: "))
second_number = int(input("Please enter your second number: "))

print("\n------------------------------------------------------------")

print("Calculating the result...")
result = first_number ** second_number
print(f"\nThe result of raising {first_number} to the power of {second_number} is: {result}")

print("------------------------------------------------------------")